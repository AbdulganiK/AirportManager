package model;

/**
 * Enum representing all continents on Earth.
 * <p>
 * Each continent is represented by its standard two-letter abbreviation:
 * </p>
 * <ul>
 *     <li>{@code AF} - Africa</li>
 *     <li>{@code AN} - Antarctica</li>
 *     <li>{@code AS} - Asia</li>
 *     <li>{@code EU} - Europe</li>
 *     <li>{@code NA} - North America</li>
 *     <li>{@code OC} - Oceania</li>
 *     <li>{@code SA} - South America</li>
 * </ul>
 *
 * @author Abdulgani Korkmaz
 * @version 1.0
 */
public enum Continent {

    /**
     * Africa.
     */
    AF,

    /**
     * Antarctica.
     */
    AN,

    /**
     * Asia.
     */
    AS,

    /**
     * Europe.
     */
    EU,

    /**
     * North America.
     */
    NA,

    /**
     * Oceania.
     */
    OC,

    /**
     * South America.
     */
    SA,
}
